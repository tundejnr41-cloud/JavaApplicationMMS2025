public class UsingMethodClass{
	public static void main(String[] args){
		System.out.println("The absolute value is " + Math.abs(-123));
		
		System.out.println("The greater number is  " + Math.max(25,84));
		System.out.println("The smaller number is  " + Math.min(25,84));
		System.out.println("The Square root of the number is  " + Math.sqrt(9801));
		System.out.println("The result of the exponentiation is  " + Math.pow(17,4));
		System.out.println("A random number between 0 and 1 is " + Math.random());
	}
}